			>회사는 회원이 다음 각호에 해당할 경우에 사전통지 없이 이용계약을 해지할 수 있으며 다음 각호에
			의해 해지된 회원이 다시 이용 신청을 하는 경우 이용 승낙을 거부하거나 사후에 이용계약을 해지할
			수 있습니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>1. 가입 신청 및 요금제 계약 시 기재한 내용이 허위로 판명된 경우</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>2. 서비스 운영을 고의로 방해하여 회사에 막대한 손해를 입힌 경우</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>3. 제16조에 의하여 이용 제한된 후 그 이용 제한 기간 내에 이용 제한 사유를 해소하지 않는 경우</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal">4. 제13조에 위반하는 경우</span>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>5. 지속적으로 불법스팸을 전송하여 정보통신부 또는 한국정보보호진흥원에서 계약 해지를 요청하는
			경우</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">③ </span><span
			style="letter-spacing:normal"
			>2항에 따른 이용계약의 해지 시 회사는 제22조에 의거하여 이용요금을 환불합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">④ </span><span
			style="letter-spacing:normal"
			>회원은 탈퇴하고자 할 경우 회사 홈페이지에서 직접 탈퇴를 진행합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">⑤ </span><span
			style="letter-spacing:normal"
			>서비스 이용요금을 결제하지 않은 체험판 회원은 즉시 가능하나 서비스 이용계약 중인 회원은
			서비스 이용계약 해지 후 탈퇴가 가능합니다.</span
		>
	</p>
	<p class="a" style=" font-size:16.5pt">
		<br /><br /><span style="font-weight:bold; letter-spacing:normal">제7장 이용요금</span>
	</p>
	<p class="a" style=" font-size:11pt">
		<br /><span style="font-weight:bold; letter-spacing:normal">제21조 (이용요금 일반원칙)</span><br
		/><span style="font-family:'Times New Roman'; letter-spacing:normal">① </span><span
			style="letter-spacing:normal"
			>서비스 이용요금의 세부 내역 및 그 변경사항은 제5조에 따라 회사의 홈페이지에 명기합니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">② </span><span
			style="letter-spacing:normal"
			>회원이 납부하여야 하는 서비스의 이용요금 종류는 다음 각호와 같습니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>1. 쇼핑몰 서비스: 쇼핑몰 서비스 이용 시 납입하는 사용료</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>2. 홈페이지 서비스: 홈페이지 서비스 이용 시 납입하는 사용료</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>3. 부가 서비스: 카카오 알림톡/문자, 마케팅 문자, 카카오페이 연동비, 무통장 입금 자동 확인,
			도메인 등 부가 서비스를 선택적으로 이용할 때 납입하는 사용료</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>4. 기타 서비스: 서비스 기본 제공량 이상 사용 혹은 서비스별 이용 조건 외 이용 시 납입하는
			사용료</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">③ </span><span
			style="letter-spacing:normal"
			>이용요금의 납부는 선납이 원칙이나 회사의 정책 혹은 서비스 종류에 따라 후납으로 할 수
			있습니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">④ </span><span
			style="letter-spacing:normal"
			>회원은 정해진 납부 일자에 이용요금을 결제해야 합니다. 이를 결제하지 못할 경우에는 제15조에
			따른 서비스 이용 제한 또는 제19조제2항에 의거 이용계약이 해지될 수 있습니다. 해지된 후 이에
			대한 재사용을 원하는 경우 회원은 다시 결제를 하면 됩니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">⑤ </span><span
			style="letter-spacing:normal"
			>이용계약기간 중 이용요금이 변경되더라도 변경 현재 진행 중인 계약 기간에 대해서는 특별한
			사정이 없는 한 소급 적용되지 않습니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<br /><span style="font-weight:bold; letter-spacing:normal">제22조 (이용요금 결제)</span>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">① </span><span
			style="letter-spacing:normal"
			>서비스 이용요금의 결제 수단은 카드와 무통장 입금이며 결제 방법은 다음 각호와 같습니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>1. 카드 결제는 회사 홈페이지 로그인 후 직접 결제를 진행합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>2. 무통장 입금은 회원이 이메일로 회사 고객센터에 무통장 입금 진행 요청을 함으로써 진행되며,
			회원은 무통장 입금 후 입금 완료 이메일을 회사 고객센터로 발신해야 합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">② </span><span
			style="letter-spacing:normal">서비스 이용요금의 결제는 다음 각호와 같습니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>1. 쇼핑몰 서비스, 홈페이지 서비스 이용요금은 회사의 홈페이지에서 결제가 가능합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>2. 부가서비스 이용요금 결제는 각 서비스별 안내에 따릅니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<br /><span style="font-weight:bold; letter-spacing:normal">제23조 (이용요금 환불)</span>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">① </span><span
			style="letter-spacing:normal"
			>서비스 이용 중 이용요금 환불은 서비스에 따라 홈페이지에서 회원이 직접 신청하거나 회사
			고객센터로 환불 요청 이메일을 접수 시 환불 가능합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">② </span><span
			style="letter-spacing:normal"
			>서비스 이용요금을 카드로 결제한 경우에는 회원이 홈페이지에서 환불 신청 시 지체 없이 당해
			결제수단을 제공한 사업자로 하여금 대금의 청구를 정지 또는 취소하도록 요청합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">③ </span><span
			style="letter-spacing:normal"
			>서비스 이용요금을 무통장 입금으로 결제한 경우에는 회원이 홈페이지에서 환불 신청 시 5영업일
			이내 환불합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="font-family:'Times New Roman'; letter-spacing:normal">④ </span><span
			style="letter-spacing:normal">서비스별 이용요금 환불은 다음 각 호와 같이 합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>1. 쇼핑몰 서비스, 홈페이지 서비스 이용요금은 결제 후 48시간 이내 홈페이지에서 환불 신청 시
			전액 환불 가능합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>2. 쇼핑몰 서비스, 홈페이지 서비스 이용 중에 중도해지를 하는 경우, 이용요금의 정산은 이용
			개통일로부터 해지 신청일까지의 이용요금을 할인율이 적용되지 않은 회사의 정상적인 월납 요금을
			기준으로 일할 계산한 금액과 이벤트로 제공된 혜택 금액을 선납한 이용요금에서 제외한 잔액을
			신청일로부터 5영업일 이내에 환불합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>* 선납 금액 - 사용 금액(정가 기준 일할 금액 x 사용 일수) - 이벤트 제공 혜택 금액 = 잔액</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>* 1년, 2년 기간제 요금의 할인율 적용은 계약 기간을 정상적인 만기 형태로 이용한다는 전제로
			제공된 것이며 중도 환불 시 할인율 적용 금액이 아닌 정가 기준 일할 금액 기준으로 계산합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>* 정가 기준 일할 금액(일할 정가 금액의 일(1)의 단위를 반올림하여 계산)</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<span style="letter-spacing:normal"
			>3. 부가, 기타 서비스 이용 요금 환불은 각 서비스 별 안내에 따릅니다.</span
		><span style=" font-size:10pt; font-weight:bold; letter-spacing:normal">제8장 기타</span>
	</p>
	<p class="a" style=" font-size:11pt">
		<br /><span style="font-weight:bold; letter-spacing:normal"
			>제24조 (손해배상의 책임 및 범위)</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">① </span><span
			style="letter-spacing:normal"
			>회사와 회원은 자신의 귀책사유로 이 약관을 위반하여 상대방에게 손해가 발생한 경우 그 손해에
			대하여 배상 책임을 부담합니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">② </span><span
			style="letter-spacing:normal"
			>다만, 서비스 중지 및 장애에 따른 손해배상은 잦은 서비스 업데이트와 인터넷 환경의 변화 등으로
			완벽한 서비스 안정성을 보장이 불가하기 때문에 서비스 요금제 기반으로 아래와 같이 배상합니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">③ </span><span
			style="letter-spacing:normal"
			>회사의 귀책사유로 회원이 서비스를 이용하지 못하는 경우에 회사는 회원이 그 사실을 회사에
			통보한 때(또는 그전에 회사가 그 사실을 알았거나 알 수 있게 된 때)로부터 계속 4시간 이상 서비스
			중지 또는 장애가 발생한 경우 그 손해배상액은 서비스 중지 또는 장애시간에 대하여 최근
			3개월(3개월 미만인 경우 해당 기간 적용)의 1일 평균 요금에 서비스 제공 중지 또는 장애시간을
			24로 나눈 수를 곱하여 산출한 금액의 5배로 합니다. 다만 단수가 1시간 미만인 경우에는 1시간으로
			합니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">④ </span><span
			style="letter-spacing:normal"
			>회사는 제휴 서비스 등 회사가 직접 운영하지 아니하는 서비스에 대해서는 그로 인한 회원의 손해에
			대하여 배상 책임을 부담하지 아니합니다.</span
		>
	</p>
	<p class="a" style=" font-size:11pt">
		<br /><span style="font-weight:bold; letter-spacing:normal">제25조 (면책)</span><br /><span
			style="font-family:'Times New Roman'; letter-spacing:normal"
			>①
		</span><span style="letter-spacing:normal"
			>회사는 회원이 회사의 서비스 제공으로부터 기대되는 이익을 얻지 못하였거나 서비스로부터
			수반되는 잠재가치 및 서비스에 대한 취사선택 또는 이용으로 발생하는 손해 등에 대해서는 책임이
			없습니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">② </span><span
			style="letter-spacing:normal"
			>회사는 회원의 귀책사유로 인하여 서비스 이용의 장애가 발생한 경우에는 책임을 지지 않습니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">③ </span><span
			style="letter-spacing:normal"
			>회사의 책임은 물리적인 호스팅 서비스 사용에 한하며, 특히 회원이 게시 또는 전송하거나 전송받은
			자료의 내용 및 가치에 대해서는 책임이 없습니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">④ </span><span
			style="letter-spacing:normal"
			>회사는 회원 상호 간 또는 이용 회원과 제3자 상호 간에 서비스를 매개로 하여 물품 거래 등을 한
			경우 해당 거래로 인해 발생하는 손실에 대해 책임을 지지 않습니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">⑤ </span><span
			style="letter-spacing:normal"
			>불법적인 침입으로 인해 발생한 회원의 설비 및 정보에 대한 손해는 배상하지 아니 합니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">⑥ </span><span
			style="letter-spacing:normal"
			>회사에서 회원에게 무료로 제공하는 서비스의 이용과 관련해서는 어떠한 손해도 책임을 지지
			않습니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">⑦ </span><span
			style="letter-spacing:normal"
			>회사는 회원과 제3자 상호 간에 서비스를 매개로 발생한 분쟁에 대해 개입할 의무가 없으며, 이로
			인한 손해를 배상할 책임도 없습니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">⑧ </span><span
			style="letter-spacing:normal"
			>회사는 회원이 제작한 쇼핑몰 또는 홈페이지를 감시하지 않으며, 본 서비스를 이용하여 타인에게
			피해를 주거나 미풍양속을 해치는 행위를 하여 발생하는 모든 법적인 책임은 회원에게 있습니다.</span
		><br /><span style="font-family:'Times New Roman'; letter-spacing:normal">⑨ </span><span
			style="letter-spacing:normal"
			>회사는 제29조에 따른 손해가 다음 각 호의 1에 의해 발생한 경우에는 책임을 지지 아니 합니다.</span
		>
	</p>
